# ============================================================================
# 第一部分：导入依赖库
# ============================================================================

# ChatOpenAI: LangChain 提供的 OpenAI 兼容聊天模型封装类
# 由于 DeepSeek API 兼容 OpenAI 格式，我们可以直接使用此类
from langchain_openai import ChatOpenAI

# ChatPromptTemplate: 用于构建结构化提示词模板
# 支持系统消息、用户消息、AI消息等多种角色
from langchain_core.prompts import ChatPromptTemplate

# load_dotenv: 从 .env 文件加载环境变量
# 用于安全地管理 API 密钥等敏感信息，避免硬编码在代码中
from dotenv import load_dotenv

# os: Python 标准库，用于读取环境变量
import os

# ============================================================================
# 第二部分：环境配置
# ============================================================================

# 加载环境变量，从 .env 文件中读取敏感配置信息
# .env 文件示例内容：
# DEEPSEEK_API_KEY=your_api_key_here
# 注意：.env 文件应添加到 .gitignore，避免提交到版本控制
load_dotenv()

# ============================================================================
# 第三部分：初始化大语言模型（LLM）
# ============================================================================

# 初始化 DeepSeek 大模型
# ChatOpenAI 参数说明：
# - model: 模型名称
#   - "deepseek-chat": 通用对话模型，适合日常问答
#   - "deepseek-coder": 代码专用模型，编程任务效果更好
# - api_key: API 密钥，从环境变量读取以保证安全
# - base_url: DeepSeek API 的端点地址
#   - /beta/v1 表示使用测试版 API
# - temperature: 温度参数，控制输出的随机性
#   - 范围：0.0 ~ 2.0
#   - 0.0: 确定性输出，适合需要精确答案的场景
#   - 0.7: 平衡创造性和准确性，适合一般对话
#   - 1.0+: 更具创造性，适合创意写作
# - max_tokens: 最大输出 token 数
#   - 1 个中文约等于 1-2 个 token
#   - 2048 个 token 约等于 1000-1500 个汉字
llm = ChatOpenAI(
    model="deepseek-chat",  # 写代码用 deepseek-coder 效果更好
    api_key=os.getenv("DEEPSEEK_API_KEY"),
    base_url="https://api.deepseek.com/beta/v1",
    temperature=0.7,
    max_tokens=2048
)

# ============================================================================
# 第四部分：调用方式演示
# ============================================================================

# ----------------------------------------------------------------------------
# 方式1：直接调用（最简单的方式）
# ----------------------------------------------------------------------------
# 适用场景：快速测试、简单问答、不需要复杂提示词的情况
# 特点：直接传入字符串，返回 AIMessage 对象

print("===== 方式1：直接调用 =====")

# invoke() 是 LangChain 的标准调用方法
# 参数：用户输入的文本（字符串）
# 返回值：AIMessage 对象，包含 content（内容）、metadata（元数据）等属性
response = llm.invoke("你好，请介绍一下自己")

# response.content 包含模型生成的文本内容
print(f"回答：{response.content}\n")

# ----------------------------------------------------------------------------
# 方式2：结合提示词模板 + LCEL 链式调用（推荐方式）
# ----------------------------------------------------------------------------
# 适用场景：需要结构化提示词、多轮对话、复杂应用
# LCEL（LangChain Expression Language）：LangChain 表达式语言
# 特点：
# - 使用 | 操作符连接组件，代码简洁直观
# - 支持流式输出、批量处理、异步调用
# - 易于组合和复用

print("===== 方式2：提示词模板 + 链式调用 =====")

# 创建提示词模板
# from_messages() 方法接受一个消息列表，每个消息是一个元组 (role, content)
# 角色（role）类型：
# - "system": 系统消息，设定 AI 的行为和角色
# - "user": 用户消息，表示用户的输入
# - "assistant": AI 消息，表示模型的回复
# - "human": 同 "user"
# - "ai": 同 "assistant"
#
# 模板变量：使用 {variable_name} 语法定义占位符
# 运行时通过字典传入实际值，如 {"question": "实际问题"}
prompt = ChatPromptTemplate.from_messages([
    ("system", "你是一位专业的Python编程助手，回答简洁明了。"),
    ("user", "{question}")  # {question} 是模板变量
])

# 使用 | 操作符构建链（LCEL - LangChain Expression Language）
# 链（Chain）是 LangChain 的核心概念，表示数据处理的流水线
# 数据流向：输入 -> prompt 模板 -> 格式化后的消息 -> llm -> 输出
# 
# 这种写法等价于：
# chain = prompt.pipe(llm)
# 或者传统方式：
# from langchain.chains import LLMChain
# chain = LLMChain(llm=llm, prompt=prompt)
chain = prompt | llm

# 调用链
# 参数：字典，键对应模板中的变量名
# 执行过程：
# 1. 将 {"question": "..."} 传入 prompt 模板
# 2. 模板生成格式化的消息列表
# 3. 消息传递给 llm 生成回复
# 4. 返回 AIMessage 对象
chain_response = chain.invoke({"question": "Python 中列表和元组有什么区别？"})
print(f"回答：{chain_response.content}\n")

# ----------------------------------------------------------------------------
# 方式3：批量调用（高效处理多个请求）
# ----------------------------------------------------------------------------
# 适用场景：需要同时处理多个相似请求，如批量生成、数据标注
# 优势：
# - 内部优化，比循环调用 invoke() 更高效
# - 自动处理并发和错误
# - 返回结果顺序与输入顺序一致

print("===== 方式3：批量调用 =====")

# 准备批量输入数据
# 每个元素是一个字典，对应一次调用的输入参数
questions = [
    {"question": "什么是装饰器？"},
    {"question": "解释一下生成器"},
    {"question": "Python 的 GIL 是什么？"}
]

# batch() 方法批量执行
# 参数：输入列表
# 返回值：结果列表，顺序与输入一致
batch_responses = chain.batch(questions)

# 使用 zip() 同时遍历问题和答案
# r.content[:50] 截取回复的前 50 个字符，避免输出过长
for q, r in zip(questions, batch_responses):
    print(f"Q: {q['question']}")
    print(f"A: {r.content[:50]}...\n")

