#!/usr/bin/env python3
"""
LangGraph 核心概念 01 - StateGraph 基础
功能：构建最简单的图，理解 State、Node、Edge 的基本概念

核心概念：
- State: 状态，整个图的共享数据容器。所有节点都可以读取和修改状态，
         状态在节点之间传递，实现数据流转。
- Node: 节点，执行任务的函数。每个节点接收当前状态，执行特定逻辑，
        返回更新后的状态。节点是图的基本执行单元。
- Edge: 边，连接节点控制流程。边定义了节点之间的执行顺序和数据流向，
        可以是有条件的（条件边）或无条件的（普通边）。

执行流程：
    start(入口) → process(处理) → end(结束)

学习要点：
1. 理解 TypedDict 如何定义状态结构
2. 掌握节点的函数签名规范：接收 state，返回 state
3. 学会使用 StateGraph 构建和编译图
4. 了解图的两种执行方式：invoke(一次性执行) 和 stream(流式执行)
"""
from typing import TypedDict
from langgraph.graph import StateGraph, END
from langchain_openai import ChatOpenAI
from dotenv import load_dotenv
import os
from pathlib import Path

# =============================================================================
# 环境配置
# =============================================================================
# 加载环境变量：从 .env 文件中读取 API 密钥等配置
# 确保在项目根目录有 .env 文件，内容如：
# DEEPSEEK_API_KEY=your_api_key_here
load_dotenv()


# =============================================================================
# 第一部分：定义状态 (State)
# =============================================================================
# 
# 【核心概念】
# State 是 LangGraph 中最重要的概念之一。它是整个图的共享数据容器，
# 所有节点都可以读取和修改这个状态。状态在节点之间传递，实现数据流转。
#
# 【为什么使用 TypedDict？】
# TypedDict 是 Python 的类型提示工具，它可以：
# 1. 定义状态的结构和字段类型
# 2. 提供 IDE 代码补全和类型检查
# 3. 让代码更易读、更易维护
#
# 【最佳实践】
# - 为每个图定义专门的状态类
# - 字段名要有意义，体现业务逻辑
# - 使用 Optional 标记可选字段

class AgentState(TypedDict):
    """
    定义图的状态结构
    
    所有节点都可以读取和修改这个状态。状态字段的设计应该反映
    业务需求，包含执行过程中需要传递和累积的所有数据。
    
    字段说明：
        input: 用户输入的问题或指令
        output: 经过处理后生成的最终答案
        step_count: 记录执行步骤数，用于调试和流程控制
    """
    input: str           # 用户输入的问题
    output: str          # 最终生成的输出结果
    step_count: int      # 步骤计数器，追踪执行进度


# =============================================================================
# 第二部分：定义节点函数 (Node)
# =============================================================================
#
# 【核心概念】
# Node（节点）是图的基本执行单元。每个节点是一个函数，接收当前状态，
# 执行特定逻辑，返回更新后的状态。
#
# 【节点函数签名规范】
# def node_name(state: StateType) -> StateType:
#     ...
#     return updated_state  # 必须返回更新后的状态
#
# 【重要提示】
# 1. 节点函数必须接收 state 参数
# 2. 节点函数必须返回一个字典（或 TypedDict），包含更新后的状态
# 3. 使用 **state 展开运算符保留原有状态，只更新需要修改的字段
# 4. 节点之间通过状态传递数据

# -----------------------------------------------------------------------------
# 节点 1：起始节点
# -----------------------------------------------------------------------------
def node_start(state: AgentState) -> AgentState:
    """
    起始节点：初始化处理
    
    这是图的入口节点，负责接收初始输入并进行初始化设置。
    在实际应用中，可以在这里进行：
    - 输入验证和预处理
    - 初始化计数器或标志位
    - 记录日志或埋点
    
    参数:
        state: 当前状态，包含用户输入和其他初始数据
        
    返回:
        更新后的状态，step_count 设置为 1 表示开始执行
        
    【代码解析】
    - **state: 展开运算符，将原状态的所有字段复制到新字典
    - "step_count": 1: 只更新 step_count 字段，其他字段保持不变
    """
    print(f"🚀 [Start] 收到输入: {state['input']}")
    return {
        **state,              # 保留原状态的所有字段
        "step_count": 1      # 更新步骤计数器为 1
    }


# -----------------------------------------------------------------------------
# 节点 2：处理节点
# -----------------------------------------------------------------------------
def node_process(state: AgentState) -> AgentState:
    """
    处理节点：调用 LLM 进行实际处理
    
    这是核心处理节点，负责调用大语言模型处理用户输入。
    在实际应用中，可以在这里进行：
    - 调用 LLM 进行推理
    - 调用外部 API 获取数据
    - 执行复杂的业务逻辑
    
    参数:
        state: 当前状态，包含用户输入和当前步骤数
        
    返回:
        更新后的状态，包含 LLM 生成的输出和递增的步骤数
        
    【代码解析】
    - ChatOpenAI: LangChain 封装的聊天模型接口
    - model: 使用的模型名称
    - api_key/base_url: API 认证信息
    - temperature: 控制输出的随机性，0.7 表示中等创造性
    - invoke(): 同步调用模型，返回 AIMessage 对象
    """
    print(f"⚙️  [Process] 正在处理... (步骤 {state['step_count']})")
    
    # 初始化 LLM 客户端
    # 这里使用 DeepSeek 的 API，可以通过环境变量配置
    llm = ChatOpenAI(
        model="deepseek-chat",                      # 模型名称
        api_key=os.getenv("DEEPSEEK_API_KEY"),      # 从环境变量读取 API 密钥
        base_url="https://api.deepseek.com/v1",     # API 基础地址
        temperature=0.7                              # 温度参数，控制创造性
    )
    
    # 构建提示词并调用 LLM
    # 在实际应用中，这里可以使用更复杂的提示词模板
    prompt = f"请用一句话回答：{state['input']}"
    response = llm.invoke(prompt)
    
    # 返回更新后的状态
    # 注意：这里同时更新了 output 和 step_count 两个字段
    return {
        **state,
        "output": response.content,                    # 存储 LLM 输出
        "step_count": state["step_count"] + 1          # 步骤计数器加 1
    }


# -----------------------------------------------------------------------------
# 节点 3：结束节点
# -----------------------------------------------------------------------------
def node_end(state: AgentState) -> AgentState:
    """
    结束节点：收尾工作和结果展示
    
    这是图的最后一个节点，负责执行收尾工作：
    - 输出最终结果
    - 记录执行日志
    - 清理临时资源
    
    参数:
        state: 当前状态，包含完整的执行结果
        
    返回:
        状态保持不变（或可以进行最后的修改）
        
    【注意】
    结束节点返回的状态不会传递给其他节点，
    但会作为 graph.invoke() 的返回值返回给调用者
    """
    print(f"✅ [End] 处理完成！总步骤: {state['step_count']}")
    print(f"📤 最终输出: {state['output'][:100]}...")  # 只显示前100个字符
    return state


# =============================================================================
# 第三部分：构建图 (Graph)
# =============================================================================
#
# 【核心概念】
# StateGraph 是 LangGraph 的核心类，用于构建和管理状态图。
# 它提供了添加节点、添加边、设置入口点等方法。
#
# 【构建流程】
# 1. 创建 StateGraph 实例，指定状态类型
# 2. 添加节点 (add_node)
# 3. 添加边，连接节点 (add_edge)
# 4. 设置入口点 (set_entry_point)
# 5. 编译图 (compile)
#
# 【重要概念：END】
# END 是 LangGraph 的特殊标记，表示图的终止。
# 当执行到达 END 时，图停止运行，返回最终状态。

def build_graph():
    """
    构建 StateGraph
    
    这是图构建的核心函数，定义了图的结构和流程。
    
    返回:
        编译后的图对象，可以调用 invoke() 或 stream() 执行
        
    【图结构示意】
    
        ┌─────────┐      ┌──────────┐      ┌───────┐      ┌─────┐
        │  Entry  │ ──▶  │  start   │ ──▶  │process│ ──▶  │ end │ ──▶ END
        └─────────┘      └──────────┘      └───────┘      └─────┘
        
    【编译的作用】
    compile() 方法会：
    1. 验证图结构的正确性（如是否有入口点、是否有死胡同）
    2. 优化执行计划
    3. 生成可执行的图对象
    """
    
    # 步骤 1: 创建 StateGraph 实例
    # 参数 AgentState 指定了状态类型，用于类型检查
    workflow = StateGraph(AgentState)
    
    # 步骤 2: 添加节点
    # add_node(name, function) 将函数注册为图的节点
    # name: 节点的唯一标识符，用于添加边时引用
    # function: 节点执行的函数
    workflow.add_node("start", node_start)      # 添加起始节点
    workflow.add_node("process", node_process)  # 添加处理节点
    workflow.add_node("end", node_end)          # 添加结束节点
    
    # 步骤 3: 添加边（连接节点）
    # add_edge(source, target) 定义节点之间的执行顺序
    # source: 源节点名称
    # target: 目标节点名称
    workflow.add_edge("start", "process")  # start 执行完后执行 process
    workflow.add_edge("process", "end")    # process 执行完后执行 end
    workflow.add_edge("end", END)          # end 执行完后结束（END 是特殊标记）
    
    # 步骤 4: 设置入口点
    # set_entry_point(name) 指定图的起始节点
    # 这是图执行的起点，类似于 main 函数
    workflow.set_entry_point("start")
    
    # 步骤 5: 编译图
    # compile() 生成可执行的图对象
    return workflow.compile()


# =============================================================================
# 第四部分：运行图 (Execution)
# =============================================================================
#
# 【执行方式】
# LangGraph 提供两种执行图的方式：
#
# 1. invoke(state): 一次性执行整个图，返回最终状态
#    适用场景：简单任务，不需要中间结果
#    
# 2. stream(state): 流式执行，返回每个节点的执行事件
#    适用场景：需要观察执行过程、调试、实时显示进度

def main():
    """
    主函数：演示图的基本执行
    
    本函数展示如何使用 invoke() 方法一次性执行整个图，
    并获取最终执行结果。
    
    【执行流程】
    1. 构建图
    2. 准备初始状态
    3. 调用 invoke() 执行图
    4. 处理返回的最终状态
    """
    # 打印标题
    print("=" * 60)
    print("LangGraph 核心概念 01 - StateGraph 基础")
    print("=" * 60)
    
    # -------------------------------------------------------------------------
    # 步骤 1: 构建图
    # -------------------------------------------------------------------------
    # 调用 build_graph() 创建图实例
    # 图只需要构建一次，可以多次执行
    graph = build_graph()
    
    # -------------------------------------------------------------------------
    # 步骤 2: 准备初始状态
    # -------------------------------------------------------------------------
    # 初始状态是图执行的起点数据
    # 必须包含 State 定义中的所有必需字段
    initial_state = {
        "input": "什么是 LangGraph？",  # 用户问题
        "output": "",                    # 初始为空，将由 LLM 填充
        "step_count": 0                  # 从 0 开始计数
    }
    
    print(f"\n📥 初始状态: {initial_state}\n")
    
    # -------------------------------------------------------------------------
    # 步骤 3: 执行图
    # -------------------------------------------------------------------------
    # invoke() 方法会：
    # 1. 从入口点开始执行
    # 2. 按边定义的流程依次执行每个节点
    # 3. 在每个节点之间传递和更新状态
    # 4. 到达 END 时停止，返回最终状态
    #
    # 【注意】
    # - invoke() 是同步执行，会阻塞直到图执行完成
    # - 如果图中包含异步操作（如 LLM 调用），invoke() 会自动处理
    final_state = graph.invoke(initial_state)
    
    # -------------------------------------------------------------------------
    # 步骤 4: 处理结果
    # -------------------------------------------------------------------------
    print(f"\n📊 最终状态:")
    print(f"  - 输入: {final_state['input']}")
    print(f"  - 输出: {final_state['output']}")
    print(f"  - 步骤数: {final_state['step_count']}")
    
    print("\n✅ 图执行完成！")
    
    # -------------------------------------------------------------------------
    # 可选：图可视化提示
    # -------------------------------------------------------------------------
    # LangGraph 支持将图结构可视化为图片
    # 需要安装 graphviz 库
    try:
        from langgraph.graph import visualize
        # 注意：需要安装额外依赖才能可视化
        print("\n💡 提示：安装 graphviz 可以可视化图结构")
        print("   pip install graphviz")
    except:
        pass


# =============================================================================
# 第五部分：进阶 - 流式执行
# =============================================================================
#
# 【stream() 方法】
# stream() 允许我们观察图的执行过程，在每个节点执行后获取中间状态。
# 这对于调试、监控和实时显示进度非常有用。
#
# 【返回的事件格式】
# stream() 返回一个迭代器，每个事件是一个字典，包含：
# - 节点名称
# - 当前状态
# - 其他元数据

def run_with_stream():
    """
    流式执行：查看每个节点的状态变化
    
    本函数展示如何使用 stream() 方法逐步执行图，
    观察每个节点执行后的状态变化。
    
    【适用场景】
    - 调试：查看每个节点的输入输出
    - 监控：追踪执行进度
    - UI 展示：实时显示执行状态
    """
    print("\n" + "=" * 60)
    print("流式执行 - 查看状态变化")
    print("=" * 60)
    
    # 重新构建图（也可以复用之前的图实例）
    graph = build_graph()
    
    # 准备不同的初始状态
    initial_state = {
        "input": "Python 有什么优点？",
        "output": "",
        "step_count": 0
    }
    
    # -------------------------------------------------------------------------
    # 流式执行
    # -------------------------------------------------------------------------
    # stream() 返回一个迭代器，每次迭代产生一个事件
    # 事件通常在以下时机产生：
    # - 节点开始执行前
    # - 节点执行完成后
    # - 遇到条件边时
    #
    # 【事件结构示例】
    # {
    #     'start': {'input': '...', 'output': '', 'step_count': 1},
    #     '__metadata__': {...}
    # }
    print("\n开始流式执行...\n")
    
    for event in graph.stream(initial_state):
        # event 是一个字典，键是节点名称，值是该节点执行后的状态
        print(f"📦 事件: {event}")
        print("-" * 40)
    
    print("\n✅ 流式执行完成！")


# =============================================================================
# 程序入口
# =============================================================================
#
# 【最佳实践】
# 使用 if __name__ == "__main__": 确保以下代码只在直接运行脚本时执行，
# 而在作为模块导入时不执行。

if __name__ == "__main__":
    # 执行主函数：演示基本执行流程
    main()
    
    # 执行流式执行函数：演示逐步执行和状态观察
    run_with_stream()


